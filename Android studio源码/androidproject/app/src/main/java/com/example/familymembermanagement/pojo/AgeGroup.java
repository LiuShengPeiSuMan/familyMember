package com.example.familymembermanagement.pojo;

/**
 * 年龄段
 */
public class AgeGroup {
    private Integer child;
    private Integer youth;
    private Integer juvenile;
    private Integer old;
    private Integer mittle;

    public Integer getChild() {
        return child;
    }

    public void setChild(Integer child) {
        this.child = child;
    }

    public Integer getYouth() {
        return youth;
    }

    public void setYouth(Integer youth) {
        this.youth = youth;
    }

    public Integer getJuvenile() {
        return juvenile;
    }

    public void setJuvenile(Integer juvenile) {
        this.juvenile = juvenile;
    }

    public Integer getOld() {
        return old;
    }

    public void setOld(Integer old) {
        this.old = old;
    }

    public Integer getMittle() {
        return mittle;
    }

    public void setMittle(Integer mittle) {
        this.mittle = mittle;
    }
}
